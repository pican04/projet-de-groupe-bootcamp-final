                   <P><i> vendredi 19 janvier 2018</i><br>
                    <strong> Le foufou ivoirien</strong><br>

                            Difficulté : Facile <br>
                            Coût : Raisonnable<br>
                            Recette pour 6 personne(s)<br>
                            Temps de préparation : 15 min<br>
                            Temps de cuisson : 30 min<br>
                            Temps de marinade : <br><br>
                           <strong> Liste des Ingrédients</strong><br><br>
                            Pour la sauce : <br>
- 1 queue de que de boeuf (coupé en morceau par le boucher)<br> 
- 2 kg de viande de boeuf ( découpé en morceaux moyens)<br> 
- 6 morceaux de poisson fumé ( machoiron... nettoyés)<br> 
- 4 crabes de lagune ou de mer <br>
- 6 aubergines Africaine (épluchées)<br> 
- 6 gombos frais équeutés <br>
- 2 oignons coupés en cube <br>
- 2 piments frais rouge<br> 
- 5 d'huile rouge ( huile de palme)<br> 
- Du piment sec en poudre <br>
- Du sel<br>  
- 1 bouillon cube<br> 
- 1 branche de kablé<br> 
- 4 l d'eau Pour les deux recettes de foufou : <br>
- 1 grosse igname (coupé en gros cubes) <br>
- 6 bananes plantains mûres ( épluchées et coupées en deux) <br>
- 1/2 l d'huile rouge (huile de palme) <br>
- Du sel fin<br><br>
<strong> Préparation</strong><br><br>
1.Je mets dans le fond de ma cocotte,la viande, la queue de boeuf, les crabes,<br> les dés d'oignons, le sel , le bouillon cube et l'eau. Je laisse cuire à feu fort jusqu'à une belle ébullition.<br> J'ajoute les cubes d'igname et je poursuis la cuisson à couvert pendant 30 min. A mi cuisson j'ajoute les morceaux de banane plantin.<br> 2.Au bout de la demie heure de cuisson, je retire les ignames et les bananes que je réserve. <br> 3.Je poursuis la cuisson de la sauce en y ajoutant les  aubergines, les gombo et les 5 cs d'huile rouge. <br>Je couvre et laisse cuire pendant 30 min à feu moyen.<br> 4.Je reviens à mes légumes en commençant par l'igname que je réduis en purée ( à l'aide de mon pilon) dans le fond de mon mortier.<br> Dès que j'obtiens  une pâte lisse et onctueuse, j'y verse de l'huile rouge , le sel, puis  je mélange intimement l'ensemble.<br> Je façonne ensuite une grosse boule ou deux. Juste pour la déco, j'y fais des trous avec les doigts <br>et je les colore de piment sec en poudre salé. <br> J'en fais  de même avec les bananes plantins sans les décorer à la fin.<br> 5.Je retourne à ma sauce. Je retire les aubergines et les piments que je mixe au robot (ou au mortier) .<br> Je reverse la purée d'aubergine dans la sauce, la feuille<br> de kablé et je poursuis la cuisson à couvert pendant encore 30 min.<br> Je rectifie au besoin l 'assaisonnement à mi cuisson.<br><br>

Suggestion<br>
Hum c'est prêt ! Je sers  le bouillon à part dans une grande soupière avec <br> tous les ingrédients accompagné du foufou d'igname et du foufou de banane plantin. 


